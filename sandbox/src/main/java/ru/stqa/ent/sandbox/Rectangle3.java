package ru.stqa.ent.sandbox;

public class Rectangle3 {
  public double p;
  public double e;

  // конструктор называется также, как класс, и не имеет возвращаемого значения (поэтому перед именем конструктора тип не указывается)
  // точно также, как функция, конструктор может принимать параметры
  public Rectangle3(double bb, double dd) {
    p = bb;
    e = dd;
  }

}
